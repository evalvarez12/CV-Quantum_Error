    case '2'
        F=...
            integralN(@(r,theta,ra,ri)...
            exp(-(ra.^2 + ri.^2)./sigma).*1/(pi*sigma).*...
            (exp(1).^((1/8).*r.*((sqrt(-1)*(-8)).*((-1)+gx).*ri.*cos(theta)+r.* ...
  ((-7)+(-1).*V+gx.^2.*((-3)+(-5).*V+4.*((-1)+V.^2).^(1/2))+gx.*(2+( ...
