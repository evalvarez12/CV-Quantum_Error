  .*gp.*(1+V)+gp.^2.*(3+V)+(-1).*gx.*(2+(-2).*V+gx.*(3+V))).*cos(2.* ...
  theta)+(sqrt(-1)*(-16)).*ra.*sin(theta)+(sqrt(-1)*(-16)).*gp.*ra.* ...
  sin(theta))).*pi.^(-1).*r)...
