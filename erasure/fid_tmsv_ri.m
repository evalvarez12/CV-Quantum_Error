  -1)*8).*((-1)+gp).*ra+r.*((-7)+(-1).*V+gp.^2.*((-3)+(-5).*V+4.*(( ...
  -1)+V.^2).^(1/2))+gp.*(2+(-2).*V+4.*((-1)+V.^2).^(1/2))).*sin( ...
